function dSTM = getSTM_dot_CR3BP(STM,r_nd,mu)
% =======================================================================
%                       Derivative of STM in CR3BP
% =======================================================================
%
% Author : Kevin Charls (jackcruose47)
%
% Last Update : 10-10-2020
%
% Format : dSTM = getSTM_dot_CR3BP(STM,r_nd,mu)
%
% -----------------------------------------------------------------------
%                               INPUTS
% -----------------------------------------------------------------------
% r_nd          : Non-dimensional position in CR3BP [3x1]
% mu            : 3-body constant [1x1]
% -----------------------------------------------------------------------
%
% -----------------------------------------------------------------------
%                              OUTPUTS
% -----------------------------------------------------------------------
% dSTM          : Derivative of STM [6x6]
% -----------------------------------------------------------------------
% 
% -----------------------------------------------------------------------
%                            CHANGE LOG
% -----------------------------------------------------------------------
% 10-10-2020 : Code Created
% -----------------------------------------------------------------------

% - Jacobian Matrix of CR3BP vector field
A = getA_CR3BP(r_nd,mu);

% - Dynamics of STM
dSTM = A*STM;

end